DROP SCHEMA PUBLIC CASCADE;

CREATE TABLE RECIPES (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
    name VARCHAR(255),
    description VARCHAR(255),
    ingredients VARCHAR(255)
);

CREATE TABLE USERS (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
    username VARCHAR(255),
    password VARCHAR(255),
    email VARCHAR(255)
);